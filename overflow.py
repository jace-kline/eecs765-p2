def little_endian(bs: bytes, wordsize=4) -> bytes:
    words = len(bs) // wordsize
    if(len(bs) % wordsize != 0):
        print("Error: Byte array must be a multiple of the wordsize")
        exit(1)
    else:
        return struct.pack(f'<{words}i', *struct.unpack(f'>{words}i', bs))

